// Dates

let date = new Date()
console.log(date.toString()); //day, date and time in IST
console.log(date.toDateString()); //gives us just the date and day
console.log(date.toISOString()); //gives us date in ISO format
console.log(date.toLocaleDateString()); //gives us date in 12/12/2024 format 
console.log(date.toLocaleString()); //gives us date and time 
console.log(date.toLocaleTimeString()); //gives us only time
console.log(date.toTimeString()); //gives us 24H time in IST

// type of date is object






